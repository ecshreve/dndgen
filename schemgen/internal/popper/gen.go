package main

import (
	"os"
	"text/template"

	"github.com/charmbracelet/log"
	"github.com/samsarahq/go/oops"
)

var TypesToPopulate = []string{
	"AbilityScore",
	"Skill",
	"Language",
	"DamageType",
	"Race",
	"Class",
	"WeaponProperty",
	"MagicSchool",
	"RuleSection",
	"Rule",
	"Subrace",
	"Trait",
	"Coin",
}

var POP_TEMPLATE = `// GENERATED BY popper
package popper

import (
	"context"
	"fmt"

	"github.com/ecshreve/dndgen/ent"
	"github.com/ecshreve/dndgen/internal/utils"
	
	"github.com/charmbracelet/log"
)
{{ range . }}
// Populate{{ . }} populates the {{ . }} entities from the JSON data files.
func (p *Popper) Populate{{ . }}(ctx context.Context) ([]*ent.{{ . }}, error) {
	fpath := "data/{{ . }}.json"
	var v []ent.{{ . }}

	if err := utils.LoadJSONFile(fpath, &v); err != nil {
		return nil, fmt.Errorf("LoadJSONFile: %w", err)
	}

	creates := make([]*ent.{{ . }}Create, len(v))
	for i, vv := range v {
		creates[i] = p.Client.{{ . }}.Create().Set{{ . }}(&vv)
	}

	created, err := p.Client.{{ . }}.CreateBulk(creates...).Save(ctx)
	if err != nil {
		return nil, fmt.Errorf("CreateBulk: %w", err)
	}
	log.Info("bulk creation success", "created", len(created), "entity", "{{ . }}")

	p.Populate{{ . }}Edges(ctx, v)

	for _, c := range created {
		p.IdToIndx[c.ID] = c.Indx
		p.IndxToId[c.Indx] = c.ID
	}

	return created, nil
}
{{ end }}
`

func main() {
	t := template.Must(template.New("pop").Parse(POP_TEMPLATE))
	// Create the output file.
	ofile, err := os.Create("populators_gen.go")
	if err != nil {
		log.Fatal(oops.Wrapf(err, "unable to create output file for populators"))
	}
	defer ofile.Close()

	if err := t.Execute(ofile, TypesToPopulate); err != nil {
		log.Fatal(oops.Wrapf(err, "unable to execute template"))
	}
}
